<script setup lang="ts">
import { ref, reactive } from 'vue';

// Form data
const formData = reactive({
  name: '',
  email: '',
  subject: 'general',
  message: '',
  agreement: false
});

// Form state
const errors = reactive({
  name: '',
  email: '',
  message: '',
  agreement: ''
});
const isSubmitting = ref(false);
const formSuccess = ref(false);

// FAQ state
const faqOpen = ref([false, false, false, false]);

// Toggle FAQ item
const toggleFaq = (index: number) => {
  faqOpen.value[index] = !faqOpen.value[index];
};

// Validate form field
const validateField = (field: string) => {
  switch (field) {
    case 'name':
      errors.name = !formData.name.trim() ? '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –≤–∞—à–µ –∏–º—è' : '';
      break;
    case 'email':
      if (!formData.email.trim()) {
        errors.email = '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –≤–∞—à email';
      } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(formData.email)) {
        errors.email = '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–π email';
      } else {
        errors.email = '';
      }
      break;
    case 'message':
      errors.message = !formData.message.trim() ? '–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤–≤–µ–¥–∏—Ç–µ –≤–∞—à–µ —Å–æ–æ–±—â–µ–Ω–∏–µ' : '';
      break;
    case 'agreement':
      errors.agreement = !formData.agreement ? '–ù–µ–æ–±—Ö–æ–¥–∏–º–æ —Å–æ–≥–ª–∞—Å–∏—Ç—å—Å—è —Å –ø–æ–ª–∏—Ç–∏–∫–æ–π –∫–æ–Ω—Ñ–∏–¥–µ–Ω—Ü–∏–∞–ª—å–Ω–æ—Å—Ç–∏' : '';
      break;
  }
};

// Validate all form fields
const validateForm = () => {
  validateField('name');
  validateField('email');
  validateField('message');
  validateField('agreement');
  
  return !Object.values(errors).some(error => error);
};

// Submit form
const submitForm = async () => {
  if (!validateForm()) {
    // Scroll to first error
    const firstErrorField = Object.keys(errors).find(key => errors[key as keyof typeof errors]);
    if (firstErrorField) {
      const element = document.getElementById(firstErrorField);
      if (element) {
        element.scrollIntoView({ behavior: 'smooth', block: 'center' });
      }
    }
    return;
  }
  
  isSubmitting.value = true;
  
  try {
    // In a real app, you would send the form data to your API
    await new Promise(resolve => setTimeout(resolve, 1500));
    
    // Show success message
    formSuccess.value = true;
    
    // Reset form
    formData.name = '';
    formData.email = '';
    formData.subject = 'general';
    formData.message = '';
    formData.agreement = false;
    
  } catch (error) {
    console.error('Error submitting form:', error);
    alert('–ü—Ä–æ–∏–∑–æ—à–ª–∞ –æ—à–∏–±–∫–∞ –ø—Ä–∏ –æ—Ç–ø—Ä–∞–≤–∫–µ —Ñ–æ—Ä–º—ã. –ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –ø–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â–µ —Ä–∞–∑.');
  } finally {
    isSubmitting.value = false;
  }
};

